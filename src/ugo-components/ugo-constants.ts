// --- src/ugo-components/ugo-constants.ts ---
// Этот файл содержит неизменные геометрические константы для всех
// Условных Графических Обозначений (УГО) в приложении.
// Эти значения являются "ГОСТом" нашего проекта и не должны изменяться
// без согласования, так как на них основана вся логика компоновки схем.
// Все размеры указаны в условных единицах (пикселях).

// --- УГО с Параллельными Валами ---
// (Цилиндрическая, Ременная, Зубчато-ременная, Цепная передачи)
// Эти УГО имеют вытянутую форму, где валы расположены параллельно.
export const UGO_PARALLEL_WIDTH = 40;    // Ширина УГО вдоль валов
export const UGO_PARALLEL_HEIGHT = 110;  // Общая высота УГО

// Координаты валов (оси Y) от верхнего края УГО (y=0).
// Эти целочисленные значения были подобраны, чтобы избежать артефактов рендеринга.
export const UGO_PARALLEL_SHAFT_Y1 = 36; // Положение "верхнего" вала
export const UGO_PARALLEL_SHAFT_Y2 = 73; // Положение "нижнего" вала

// Расстояние между осями валов. Используется для расчетов смещения.
export const UGO_PARALLEL_SHAFT_DISTANCE = UGO_PARALLEL_SHAFT_Y2 - UGO_PARALLEL_SHAFT_Y1; // = 37

// --- УГО Соосные и Поворотные ---
// (Планетарная, Коническая, Червячная передачи)
// Эти УГО имеют квадратную форму.
export const UGO_COAXIAL_TURNING_DIM = 100; // Ширина и высота УГО

// Точка подключения для этих УГО всегда находится в центре.
export const UGO_COAXIAL_TURNING_CENTER_XY = UGO_COAXIAL_TURNING_DIM / 2; // = 50
